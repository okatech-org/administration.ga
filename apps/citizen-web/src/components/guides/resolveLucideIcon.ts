/**
 * Resout un nom d'icone en composant Lucide.
 * Retourne HelpCircle si le nom n'est pas reconnu.
 */
export function resolveLucideIcon(name: string): LucideIcon {
  return ICON_MAP[name] ?? HelpCircle;
}
